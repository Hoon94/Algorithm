def C2F(degree):
	return float(degree) * 9 / 5 + 32

def F2C(degree):
	return (float(degree) - 32) * 5 / 9

def C2K(degree):
	return float(degree) + 273.15

def K2C(degree):
	return float(degree) - 273.15

def F2K(degree):
	return (float(degree) - 32) * 5 / 9 + 273.15

def K2F(degree):
	return (float(degree) - 273.15) * 9 / 5 + 32
